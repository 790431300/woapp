沃RD，一个暂时用来开发，学习编程的应用软件，适合运行在树莓派上，其他环境也可以，安全不敢保证，所以最好不要运行在服务器，采用PHP开发，树莓派扩展空间大，本应用也一样，希望这个软件能帮助到你。<br/><br/>
怎样开发一个应用？新建文件夹，把程序写好后，添加一个wo.ini的文件，配置应用信息，例如<br/><br/>
[worobot]<br/>
 title="人工智障"<br/>
 url="games/robot/index.php"<br/>
 icon="system/image/robot.jpg"<br/>
 version="0.01"<br/>
 key="woapp"<br/><br/>
如果有数据库的话，建立叫wo.sql的文件，把MySQL数据填写进去，最后左侧有一个叫生成应用的按钮，点击就会生成一个.wo后缀的文件，这个文件就是应用包，发给别人，他就能下载安装啦，是不是很简单呢？<br/>主目录有一个WOAPP文件夹，里面有一个安装包，如果不懂，可以解压看一看它的结构。<br/>更多信息<br/>
